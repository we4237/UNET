def DepthNorm(depth,max=20,min=0):
    # normalize
    min = depth.min() if min is None else min
    max = depth.max() if max is None else max
    normalized_tensor = (depth - min) / (max - min)

    return normalized_tensor

class AverageMeter(object):
    def __init__(self):
        self.reset()

    def reset(self):
        self.val = 0
        self.avg = 0
        self.sum = 0
        self.count = 0

    def update(self, val, n=1):
        self.val = val
        self.sum += val * n
        self.count += n
        self.avg = self.sum / self.count

